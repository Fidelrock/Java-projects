public class C{

    private String name;
    private int empID;
    private int age;

    public String getname(){

        return name;
    }

    public void setname(String newname){

        name = newname;

    }

    public int getempID(){

        return empID;
    }

    public void setempID(int newempID){
        empID = newempID;
    }

    public int getage(){

        return age;
    }

    public void setage(int newage){
        age = newage;

    }
    


}