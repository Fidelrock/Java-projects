//String character at
public class CharacterAt{
    public static void main(String[]args){
        String greetings = "Hello World";
        System.out.println(greetings.charAt(0));
        System.out.println(greetings.charAt(1));
        System.out.println(greetings.charAt(2));
        System.out.println(greetings.charAt(3));
        System.out.println(greetings.charAt(4));
        
    }
}